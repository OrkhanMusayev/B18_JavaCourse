public class LiraToUSD {

    public static void main(String[] args) {

        int Lira = 1000;
        double USD = Lira * 0.16097;
        System.out.println(Lira + " Liras is equal to " + USD + " USD");

        Lira = 1000000;
        double usd = Lira * 0.16097;
        System.out.println(Lira + " Liras is equal to " + usd + " USD");

        System.out.println("====================================================");

        int Usd = 100000;
        double manat = Usd * 1.70;
        System.out.println(Usd + " USD is equal to " + manat + " manat");




    }
}
