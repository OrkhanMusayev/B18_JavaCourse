package SELF_Practice.REPLIT_REPEAT;

/**
 * use a for loop to print numbers from 100 to 0
 *
 * example:
 * 100
 * 99
 * 98
 * ...
 * 3
 * 2
 * 1
 */
public class ForLoopCountDownTo_0 {

    public static void main(String[] args) {

        for(int i = 100; i > 0; i--){
            System.out.println(i);
        }



    }
}
