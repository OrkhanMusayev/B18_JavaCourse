package SELF_Practice.REPLIT_REPEAT;

/**
 * use a while loop to print numbers from 0 to 100
 *
 * example:
 * 0
 * 1
 * 2
 * 3
 * 4
 * 5
 * ...
 * 99
 * 100
 */
public class replit_077 {

    public static void main(String[] args) {

        int x = 0;

        while(x <=100){
            System.out.println(x);
            x++;
        }
    }
}
