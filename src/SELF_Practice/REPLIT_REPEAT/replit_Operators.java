package SELF_Practice.REPLIT_REPEAT;

/**
 * using the giving x and y int variables, use operators to do math operations with the two variables. output the math operation results in this order:
 * plus,minus,multiply(*),divide(/) and modulo(%)
 * use System.out.println to output the results.
 */
public class replit_Operators {

    public static void main(String[] args) {


        int x=10;
        int y = 2;

        int plus = x+y;
        int minus = x-y;
        int multiply = x*y;
        int divide = x/y;
        int module = x%y;

        System.out.println(plus);
        System.out.println(minus);
        System.out.println(multiply);
        System.out.println(divide);
        System.out.println(module);




    }
}
