package SELF_Practice.StringClass_Manipulations;

public class STringToLowerCase_tasks {

    public static void main(String[] args) {

        String name = "AMAR MUSAYEV";
        name.toLowerCase(); // AMAR MUSAYEV
        System.out.println(name);

        name = name.toLowerCase(); // amar musayev
        System.out.println(name);

    }
}
