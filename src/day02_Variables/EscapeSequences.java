package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("\tOrkhan");
        System.out.println("Orkhan");
        // \t - means a paragraph space
        System.out.println("Cybertek School");
        System.out.println("cybertek \nSchool\n");

        System.out.println("My\nname\nis\nOrkhan");

             // \n - means new line,starts with ne LINE

        // print: My favorite TV show is "Game of Thrones"

        System.out.println("My favorite TV show is \"Game of Thrones\"");

               // \" - means double quote,prints on the console

        System.out.println("\\");
             // \\ - prints single \ on the console
        System.out.println("/");

        System.out.println("My favorite book is 'Java'"); // \' - prints the single code
        System.out.println("My favorite book is \'Java\'"); // prints the single code



    }
}
